class Customer {
  constructor(id, category, name, lastname, phone, email, image, address, location) {
    this.id = id;
    this.category = category;
    this.name = name;
    this.lastname = lastname;
    this.phone = phone;
    this.email = email;
    this.image = image;
    this.address = address;
    this.location = location;
  }
}

export default Customer;
